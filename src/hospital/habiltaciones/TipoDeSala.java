public class TipoDeSala {
int saladeespera;
int consultorios;
int saladeoperaciones;
int saladepartos;
int baños;
int bodega;

    public int getSaladeespera() {
        return saladeespera;
    }

    public void setSaladeespera(int saladeespera) {
        this.saladeespera = saladeespera;
    }

    public int getConsultorios() {
        return consultorios;
    }

    public void setConsultorios(int consultorios) {
        this.consultorios = consultorios;
    }

    public int getSaladeoperaciones() {
        return saladeoperaciones;
    }

    public void setSaladeoperaciones(int saladeoperaciones) {
        this.saladeoperaciones = saladeoperaciones;
    }

    public int getSaladepartos() {
        return saladepartos;
    }

    public void setSaladepartos(int saladepartos) {
        this.saladepartos = saladepartos;
    }

    public int getBaños() {
        return baños;
    }

    public void setBaños(int baños) {
        this.baños = baños;
    }

    public int getBodega() {
        return bodega;
    }

    public void setBodega(int bodega) {
        this.bodega = bodega;
    }

}
